#ifndef TIME_H
#define TIME_H

#include <string>	

///----------------------------------------------------------------------------------------------------
/// Time Namespace
///----------------------------------------------------------------------------------------------------
namespace Time
{
	///----------------------------------------------------------------------------------------------------
	/// GetTimestamp:
	/// 	Returns the current timestamp.
	///----------------------------------------------------------------------------------------------------
	long long GetTimestamp();

	///----------------------------------------------------------------------------------------------------
	/// GetMilliseconds:
	/// 	Returns the current milliseconds.
	///----------------------------------------------------------------------------------------------------
	int GetMilliseconds();

	///----------------------------------------------------------------------------------------------------
	/// LastModifiedToTimestamp:
	/// 	Converts a "Last-Modified" HTTP header to a timestamp.
	///----------------------------------------------------------------------------------------------------
	long long LastModifiedToTimestamp(const std::string& aLastModified);

	///----------------------------------------------------------------------------------------------------
	/// GetYear:
	/// 	Returns the year.
	///----------------------------------------------------------------------------------------------------
	int GetYear();

	///----------------------------------------------------------------------------------------------------
	/// GetMonth:
	/// 	Returns the month 1-12.
	///----------------------------------------------------------------------------------------------------
	int GetMonth();

	///----------------------------------------------------------------------------------------------------
	/// GetDay:
	/// 	Returns the day of the month 1-31.
	///----------------------------------------------------------------------------------------------------
	int GetDay();

	///----------------------------------------------------------------------------------------------------
	/// GetWeekday:
	/// 	Returns the current weekday starting Sunday = 0.
	///----------------------------------------------------------------------------------------------------
	int GetWeekday();
}

#endif
