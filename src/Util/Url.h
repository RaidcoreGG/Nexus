#ifndef URL_H
#define URL_H

#include <string>

///----------------------------------------------------------------------------------------------------
/// URL Namespace
///----------------------------------------------------------------------------------------------------
namespace URL
{
	///----------------------------------------------------------------------------------------------------
	/// UsingHTTPS:
	/// 	Returns true, if the used protocol is HTTPS.
	///----------------------------------------------------------------------------------------------------
	bool UsingHTTPS(const std::string& aUrl);

	///----------------------------------------------------------------------------------------------------
	/// GetBase:
	/// 	Extracts the base from a URL.
	///----------------------------------------------------------------------------------------------------
	std::string GetBase(const std::string& aUrl);

	///----------------------------------------------------------------------------------------------------
	/// GetEndpoint:
	/// 	Extracts the endpoint from a URL.
	///----------------------------------------------------------------------------------------------------
	std::string GetEndpoint(const std::string& aUrl);

	///----------------------------------------------------------------------------------------------------
	/// GetQuery:
	/// 	Combines an endpoint and parameters into a full query.
	///----------------------------------------------------------------------------------------------------
	std::string GetQuery(const std::string& aEndpoint, const std::string& aParameters);

	///----------------------------------------------------------------------------------------------------
	/// GetFilename:
	/// 	Extracts a filename from a URL without the extension.
	///----------------------------------------------------------------------------------------------------
	std::string GetFilename(const std::string& aUrl);
}

#endif
