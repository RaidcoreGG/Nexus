#ifndef ILOGGER_H
#define ILOGGER_H

#include "LogEnum.h"
#include "LogMsg.h"

///----------------------------------------------------------------------------------------------------
/// ILogger Interface Class
///----------------------------------------------------------------------------------------------------
class ILogger
{
	public:
	///----------------------------------------------------------------------------------------------------
	/// GetLogLevel:
	/// 	Gets the log level.
	///----------------------------------------------------------------------------------------------------
	ELogLevel GetLogLevel();

	///----------------------------------------------------------------------------------------------------
	/// MsgProc:
	/// 	Message processing function.
	///----------------------------------------------------------------------------------------------------
	virtual void MsgProc(const LogMsg_t* aLogEntry) = 0;

	protected:
	///----------------------------------------------------------------------------------------------------
	/// SetLogLevel:
	/// 	Sets the log level. Controls which messages should be received.
	///----------------------------------------------------------------------------------------------------
	void SetLogLevel(ELogLevel aLogLevel);

	private:
	ELogLevel Level = ELogLevel::ALL;
};

#endif
