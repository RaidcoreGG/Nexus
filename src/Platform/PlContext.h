#pragma once

#include <filesystem>
#include <memory>
#include <windows.h>

#include "CrashHandler/CrashHandler.h"
#include "RawInput/RiApi.h"

///----------------------------------------------------------------------------------------------------
/// Raidcore::Nexus::Platform Namespace
///----------------------------------------------------------------------------------------------------
namespace Raidcore::Nexus::Platform
{
	///----------------------------------------------------------------------------------------------------
	/// Context Class
	///----------------------------------------------------------------------------------------------------
	class Context
	{
		public:
		///----------------------------------------------------------------------------------------------------
		/// ctor
		///----------------------------------------------------------------------------------------------------
		Context(std::filesystem::path aCrashlog, std::filesystem::path aCrashstack);

		///----------------------------------------------------------------------------------------------------
		/// Shutdown:
		/// 	Shuts down the game context.
		///----------------------------------------------------------------------------------------------------
		void Shutdown();

		///----------------------------------------------------------------------------------------------------
		/// Module:
		/// 	Returns the module handle of Nexus.
		///----------------------------------------------------------------------------------------------------
		HMODULE Module();

		///----------------------------------------------------------------------------------------------------
		/// Window:
		/// 	Returns the window handle.
		///----------------------------------------------------------------------------------------------------
		HWND Window();

		///----------------------------------------------------------------------------------------------------
		/// CrashHandler:
		/// 	Returns the crash handler instance.
		///----------------------------------------------------------------------------------------------------
		Platform::CrashHandler& CrashHandler();

		///----------------------------------------------------------------------------------------------------
		/// RawInput:
		/// 	Returns the raw input API instance.
		///----------------------------------------------------------------------------------------------------
		Platform::RawInputApi& RawInput();

		private:
		HMODULE _Module      { nullptr };
		HWND    _WindowHandle{ nullptr };

		std::filesystem::path CrashLogPath{};
		std::filesystem::path CrashStackPath{};

		std::unique_ptr<Platform::CrashHandler> _CrashHandler{ nullptr };
		std::unique_ptr<Platform::RawInputApi>  _RawInputApi { nullptr };
	};
}
