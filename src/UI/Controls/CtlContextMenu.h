#ifndef CONTROLS_CONTEXTMENU_H
#define CONTROLS_CONTEXTMENU_H

#include <string>

///----------------------------------------------------------------------------------------------------
/// IContextMenu Interface Class
///----------------------------------------------------------------------------------------------------
class IContextMenu
{
	public:
	///----------------------------------------------------------------------------------------------------
	/// Render:
	/// 	Render callback for the context menu.
	///----------------------------------------------------------------------------------------------------
	void Render();

	///----------------------------------------------------------------------------------------------------
	/// GetID:
	/// 	Gets the ID of the context menu.
	///----------------------------------------------------------------------------------------------------
	const std::string& GetID() const;

	///----------------------------------------------------------------------------------------------------
	/// OpenContextMenu:
	/// 	Notifies the context menu that it should open next frame.
	///----------------------------------------------------------------------------------------------------
	void OpenContextMenu();

	protected:
	///----------------------------------------------------------------------------------------------------
	/// SetID:
	/// 	Sets the ID of the context menu.
	///----------------------------------------------------------------------------------------------------
	void SetID(std::string aID);

	///----------------------------------------------------------------------------------------------------
	/// Render:
	/// 	Callback for the context menu contents.
	///----------------------------------------------------------------------------------------------------
	virtual void RenderContent() = 0;

	private:
	std::string  ID;
	bool         ShouldOpenNextFrame = false;
};

#endif
